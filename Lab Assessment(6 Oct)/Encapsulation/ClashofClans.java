package encap;

public class ClashofClans {
	private String loot;
	private int elixir;
	private int coins;
	
	
	public void setLoot(String newloot) {
		loot = newloot;
	}

	
	public String getLoot() {
		return loot;
	}
	
	
	public void setElixir(int newElixir) {
		elixir = newElixir;
	}


	public int getElixir() {
		return elixir;
	}
	
	
	public void setCoins(int newCoins) {
		coins = newCoins;
	}


	public int getCoins() {
		return coins;
	}
	
}
